/*******************************************************************************
*函数名称：OUTPUT_S_TIME
*函数功能：输出一个带有时间上栏的菜单
*说明：
*******************************************************************************/

void OUTPUT_S_TIME(uint8 FileName[11])
{
    uchar buffer[512];
    uint i;
    INIT_SPD2701();//HW RESET and Initialize setting
    SPI4W_WRITECOM(0x24);
//=========阶段1=========================================
    OperateFile_EX(FileName,0,buffer);

    WRITE_S_TIME((time.m%10),108,buffer);
    WRITE_S_TIME((time.m/10),216,buffer);
    for(i=0;i<432;i++)
    {
        SPI4W_WRITEDATA(buffer[i]);
    }
//=========阶段2=========================================
    OperateFile_EX(FileName,432,buffer);
    if(TIME_FLAG&BIT2||(time.h>12))
    {
        WRITE_S_TIME(((time.h-12)%10),0,buffer);
        WRITE_S_TIME(((time.h-12)/10),108,buffer);
    }
    else
    {
        WRITE_S_TIME((time.h%10),0,buffer);
        WRITE_S_TIME((time.h/10),108,buffer);
    }
    
    
    for(i=0;i<504;i++)
    {
        SPI4W_WRITEDATA(buffer[i]);
    }
//=========阶段3=========================================
    OperateFile_EX(FileName,936,buffer);

    for(i=0;i<504;i++)
    {
        SPI4W_WRITEDATA(buffer[i]);
    }
//=========阶段4=========================================
    OperateFile_EX(FileName,1440,buffer);
    WRITE_S_TIME((time.day%10),288,buffer);
    WRITE_S_TIME((time.day/10),396,buffer);

    for(i=0;i<504;i++)
    {
        SPI4W_WRITEDATA(buffer[i]);
    }
//=========阶段5=========================================
    OperateFile_EX(FileName,1944,buffer);

    WRITE_S_TIME((time.month%10),216,buffer);
    WRITE_S_TIME((time.month/10),324,buffer);
    for(i=0;i<504;i++)
    {
        SPI4W_WRITEDATA(buffer[i]);
    }
//=========阶段6=========================================
    OperateFile_EX(FileName,2448,buffer);

    WRITE_S_TIME((time.year%10),144,buffer);
    WRITE_S_TIME(((time.year%100)/10),252,buffer);
    WRITE_S_TIME(((time.year%1000)/100),360,buffer);
    for(i=0;i<468;i++)
    {
        SPI4W_WRITEDATA(buffer[i]);
    }
        
//=========阶段7=========================================
    OperateFile_EX(FileName,2916,buffer);

    
    WRITE_S_TIME((time.year/1000),0,buffer);
    for(i=0;i<180;i++)
    {
        SPI4W_WRITEDATA(buffer[i]);
    }
    SPI4W_WRITECOM(0x20);
    READBUSY();
    enterdeepsleep();//默认进入深度睡眠
    

   


}

